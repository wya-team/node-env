const fs = require('fs');
const path = require('path');
const MFS = require('memory-fs');
const webpack = require('webpack');
const koaWebpack = require('koa-webpack');

const clientConfig = require('./webpack.client.config');
const serverConfig = require('./webpack.server.config');

const readFile = ($fs, file) => {
	try {
		return JSON.parse($fs.readFileSync(path.join(clientConfig.output.path, file), 'utf-8')); // eslint-disable-line
	} catch (e) {
		console.error(e);
	}
	return {};
};

const setupDevServer = (template, cb) => {
	let bundle; let clientManifest; let 
		ready;
	const readyPromise = new Promise(r => { ready = r }) // eslint-disable-line

	const update = () => {
		if (bundle && clientManifest) {

			// 注意：这里配合HMR重新生成新的renderer
			cb(bundle, { template, clientManifest });

			// 仅第一次有效
			if (ready) {
				ready();
				ready = null;
			}
		}
	};

	// client, 数组可以动态注入 如：['webpack-hot-middleware/client', clientConfig.entry.app]
	clientConfig.entry.app = [clientConfig.entry.app];
	clientConfig.output.filename = '[name].js';

	const mfs = new MFS();
	const clientCompiler = webpack(clientConfig);
	clientCompiler.outputFileSystem = mfs;
	clientCompiler.plugin('done', stats => {
		stats = stats.toJson();
		stats.errors.forEach(err => console.error(err));
		stats.warnings.forEach(err => console.warn(err));
		if (stats.errors.length) return;

		// in-memory filesystem
		clientManifest = readFile(
			mfs,
			'vue-ssr-client-manifest.json'
		);

		update();
	});

	// 注意webpack-dev-middleware@3.0的api, 其他版本有变更
	const devClient = koaWebpack({ 
		compiler: clientCompiler,
		hotClient: {
			allEntries: true
		},
		devMiddleware: {
			logLevel: 'silent',
			serverSideRender: true,
			publicPath: clientConfig.output.publicPath,
		}
	});

	// server
	const serverCompiler = webpack(serverConfig);
	serverCompiler.outputFileSystem = mfs;

	serverCompiler.watch({}, (err, stats) => { // eslint-disable-line
		if (err) throw err;
		stats = stats.toJson();
		if (stats.errors.length) return;
		// read bundle generated by vue-ssr-webpack-plugin
		bundle = readFile(mfs, 'vue-ssr-server-bundle.json');
		update();
	});

	return {
		ready: readyPromise,
		server: devClient
	};
};

module.exports = setupDevServer;
